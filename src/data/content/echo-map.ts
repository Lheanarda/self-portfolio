type ContactId = keyof typeof contacts;

function field(
  config: Omit<EchoMapRadarField, "contacts"> & { contactIds: readonly ContactId[] },
): EchoMapRadarField {
  const { contactIds, ...fieldConfig } = config;
  return { ...fieldConfig, contacts: contactIds.map((id) => contacts[id]) };
}

